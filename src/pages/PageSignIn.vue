<template>
  <v-sheet class="px-5" style="padding-top: 7%; padding-bottom: 7%">
    <v-card class="mx-auto px-6 py-8" max-width="580">
      <p class="mb-2"><strong>Sign In</strong></p>
      <v-form v-model="form" @submit.prevent="onSubmit">
        <v-text-field v-model="email" :readonly="loading" :rules="[required]" class="mb-2" label="Email"
          clearable></v-text-field>

        <v-text-field v-model="password" :readonly="loading" :rules="[required]" label="Password"
          placeholder="Enter your password" clearable></v-text-field>
        <v-checkbox v-model="terms" color="secondary" label="Remember me"></v-checkbox>
      

        <v-btn :disabled="!form" :loading="loading" color="primary" size="large" type="submit" variant="elevated" block>
          Sign In
        </v-btn>
      </v-form>
    </v-card>
  </v-sheet>
</template>
<script setup>
import { ref } from 'vue'

const form = ref(false)
const email = ref(null)
const password = ref(null)
const loading = ref(false)

function onSubmit() {
  if (!form.value) return
  loading.value = true
  setTimeout(() => (loading.value = false), 2000)
}
function required(v) {
  return !!v || 'Field is required'
}


</script>